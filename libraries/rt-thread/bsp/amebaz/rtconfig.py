    LFLAGS += ' -Wl,-wrap,rtl_printf'

    CPATH = ''
    LPATH = ''

    if BUILD == 'debug':
        CFLAGS += ' -Os -gdwarf-2'
        AFLAGS += ' -gdwarf-2'
